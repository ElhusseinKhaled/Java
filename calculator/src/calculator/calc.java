    public float calculat(char ope){
        float num1;
        num1 = Float.parseFloat(txtNum1.getText());
        
        float num2;
        num2 = Float.parseFloat(txtNum2.getText());
        
        float result;
        
        switch (ope) {
            case '+':
                result = num1 + num2;
                break;
            case '-':
                result = num1 - num2;
                break;
            case '*':
                result = num1 * num2;
                break;
            case '/':
                result = num1 / num2;
                break;
            default:
                result = num1 % num2;
                break;
        }
                
        
        RR.setText(String.valueOf(result));
        
        return 0;
    }
    

